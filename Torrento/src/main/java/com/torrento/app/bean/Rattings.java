package com.torrento.app.bean;

public class Rattings {
	
	private String timeOfRattings;
	private String dateOfRattings;
	private int rattings;
	private String outletCode;
	public String getTimeOfRattings() {
		return timeOfRattings;
	}
	public void setTimeOfRattings(String timeOfRattings) {
		this.timeOfRattings = timeOfRattings;
	}
	public String getDateOfRattings() {
		return dateOfRattings;
	}
	public void setDateOfRattings(String dateOfRattings) {
		this.dateOfRattings = dateOfRattings;
	}
	public int getRattings() {
		return rattings;
	}
	public void setRattings(int rattings) {
		this.rattings = rattings;
	}
	public String getOutletCode() {
		return outletCode;
	}
	public void setOutletCode(String outletCode) {
		this.outletCode = outletCode;
	}
	
	

}

